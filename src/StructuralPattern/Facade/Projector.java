package StructuralPattern.Facade;

public class Projector {
	public void on() {
        System.out.println("Projector를 켭니다.");
    }


    public void off() {
        System.out.println("Projector를 종료합니다.");
    }

    public void movieMode() {
        System.out.println("Projector를 영화관모드로 설정합니다.");
    }
}

